class FirstClass {
    public static void main(String args[]){
        System.out.println("Hello Java World!");
        final float pi = 3.14F; //here 'F' at the end of 3.14 is to indicate that value is float (upto 7 decimal points) because the default floating point in java is double (upto 16 decimal points)
        System.out.println(pi);
    }
}